package com.anacleto.parsing.source;

public class PdfTocConfigException extends Exception
{
    private static final long serialVersionUID = 0x2365d1215b43fab7L;

    public PdfTocConfigException()
    {
    }

    public PdfTocConfigException(String arg0)
    {
        super(arg0);
    }

    public PdfTocConfigException(String arg0, Throwable arg1)
    {
        super(arg0, arg1);
    }

    public PdfTocConfigException(Throwable arg0)
    {
        super(arg0);
    }

}
