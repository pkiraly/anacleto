package com.anacleto.parsing.source;

public class ContentReadException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7631577159116683893L;

	public ContentReadException() {
		super();
	}

	public ContentReadException(String message, Throwable cause) {
		super(message, cause);
	}

	public ContentReadException(String message) {
		super(message);
	}

	public ContentReadException(Throwable cause) {
		super(cause);
	}
}
